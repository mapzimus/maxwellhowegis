def resolve_for_finding(obj, manifest=None):
    """Provenance for any finding object (has .level, .metric): catalog metric first,
    then synthetic-check id, then the finding's layer source."""
    m = manifest or load_manifest()
    r = resolve(obj.metric, m)
    if r and r.get("resolution") not in (None, "unresolved"):
        return r
    if obj.metric in SYNTHETIC_SOURCES:
        return dict(SYNTHETIC_SOURCES[obj.metric])
    if getattr(obj, "level", None) in LAYER_SOURCES:
        return dict(LAYER_SOURCES[obj.level])
    return r
